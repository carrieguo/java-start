package com.guo.ui;

import javax.swing.*;

public class RegisterJframe extends JFrame {
    //与注册相关的代码
    public RegisterJframe(){
        this.setSize(488,500);
        //设置界面的标题
        this.setTitle("拼图 注册");
        //设置界面置顶
        this.setAlwaysOnTop(true);
        //设置界面居中
        this.setLocationRelativeTo(null);
        //设置关闭模式
        this.setDefaultCloseOperation(3);
        //显示出来
        this.setVisible(true);
    }

}
